package Poupanca;

public class JavaInvest {

	private double poupanca = 0.0;
	private double poupancaTotal = 0.0;
	private double valorDepositado = 0.0;
	private double valorTotalInvestido = 0.0;
	private double conversaoAnual = 0.0;
	
	
	public double getPoupanca() {
		return poupanca;
	}
	public void setPoupanca(double poupanca) {
		this.poupanca = poupanca;
	}
	public double getPoupancaTotal() {
		return poupancaTotal;
	}
	public void setPoupancaTotal(double poupancaTotal) {
		this.poupancaTotal = poupancaTotal;
	}
	
	public double getValorDepositado() {
		return valorDepositado;
	}
	public void setValorDepositado(double valorDepositado) {
		this.valorDepositado = valorDepositado;
	}
	public double getValorTotalInvestido() {
		return valorTotalInvestido;
	}
	public void setValorTotalInvestido(double valorTotalInvestido) {
		this.valorTotalInvestido = valorTotalInvestido;
	}
	public double getConversaoAnual() {
		return conversaoAnual;
	}
	public void setConversaoAnual(double conversaoAnual) {
		this.conversaoAnual = conversaoAnual;
	}
		
}
